package assignments;
// Assignment 5 Update the value for each data type
public class DataTypes_08 {

	public static void main(String[] args) 
	{
		byte a=10;  //Declaration/Initialization
		//OR
		byte b;  //Declaration_
		b=20;    //Initialization
        System.out.println(a);
        System.out.println(b);
        
        
        short a1=5654;
        System.out.println(a1);
        
        int b1=8763;
        System.out.println(b1);
        
        long c1=654351;
        System.out.println(c1);
        
        float d1=76.45f; //upto 5 decimal values
        System.out.println(d1);
        
        double e1=765.86; //upto 15 decimal values
        System.out.println(e1);
        
        boolean f1=true;
        System.out.println(f1);
        boolean f2=false;
        System.out.println(f2);
        
        char g1='A';
        System.out.println(g1);
        
        String name="My name is ritu";
        System.out.println(name);
        
        double pi=Math.PI;
        System.out.println(pi);
	}

}
